#include "Projectile.h"
#include <raymath.h>

std::vector<Projectile> projectiles;

void UpdateProjectiles(float deltaTime) 
{
	for (int i = 0; i < projectiles.size(); i++)
	{
		projectiles[i].Update(deltaTime);
		if (!projectiles[i].alive)
			projectiles.erase(projectiles.begin() + i);
	}
}

void DrawProjectiles()
{
	for (auto& p : projectiles)
	{
		p.Draw();
	}
}

void Projectile::Spawn() 
{
	startpos = position;
	startTime = GetTime();
	time = 0;
}

void Projectile::Destroy()
{
	if (!alive)
		return;

	alive = false;
}


void Projectile::Update(float deltaTime)
{
	if (!alive)
		return;
	Vector2 oldPosition = position;
	time += deltaTime;

	if (type == ShotType::Laser && ownerPos != nullptr)
	{
		position = *ownerPos;
		float currentAngle = *ownerRotation;
		velocity.x = cosf(currentAngle * DEG2RAD);
		velocity.y = sinf(currentAngle * DEG2RAD);
	}

	if (trajectory)
	{
		position = trajectory->GetPosition(*this, time);
		travel += Vector2Distance(oldPosition, position);
	}
	
	if (type == ShotType::Laser)
	{
		if (time >= 2.0f) Destroy();
	}
	else if (travel >= stats.distance)
	{
		Destroy();
	}
}

void Projectile::Draw()
{
	if (!alive)
		return;

	switch (type)
	{
	case ShotType::Bullet:
		DrawCircleV(position, stats.size, stats.color);
		break;
	case ShotType::Laser:
		if (trajectory)
		{
			// ????????? ?????????, ? ???? ??????????? ????? (??? ??????, ??? ???????? ?????)
			const int segments = 30;
			// ???? ???? ??? ??????? ????????
			float step = 0.05f;

			Vector2 prevPoint = position; // ????????? ??? ???????? ??????? (??? ????'????? ?? ??????)

			for (int i = 1; i <= segments; i++)
			{
				// ?????????? ??????? «??????????» ????? ?????? ?? ??????????
				// ?? ??????? (i * step) ?? ????????? ???????????? ???? ???????
				Vector2 nextPoint = trajectory->GetPosition(*this, time + (i * step));

				// ??????? ????? ??? ??????????? ?? ????? ??????
				DrawLineEx(prevPoint, nextPoint, stats.size, stats.color);

				prevPoint = nextPoint;

				// ???????????: ????????? ?????????, ???? ??????? ?????? ?? ???? ?????????
				if (Vector2Distance(position, nextPoint) >= stats.distance) break;
			}
		}
		else
		{
			// ???? ?????????? ????? — ??????? ????????? ?????? ?????
			Vector2 endPos = Vector2Add(position, Vector2Scale(velocity, stats.distance));
			DrawLineEx(position, endPos, stats.size, stats.color);
		}
		break;
	default:
		break;
	}
}